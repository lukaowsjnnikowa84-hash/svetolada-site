def get_soul_path(age: int, gender: str, has_time: bool):
    key = (
        "Путь души — это тихий внутренний вектор, который ведёт вас туда, "
        "где вы становитесь собой. Это не задача и не обязанность, "
        "а естественное направление, в котором ваша энергия раскрывается мягко и свободно.\n\n"
        "Каждая душа приходит в этот мир со своим направлением. Не с готовым планом и не с жёсткой задачей, "
        "а с тонким внутренним импульсом, который проявляется через желания, уроки, повторяющиеся ситуации, "
        "кармические узлы и то, что вы делаете естественно, не задумываясь.\n\n"
        "Путь души раскрывается постепенно — через людей, через выборы, через ошибки, через радости, через тень, через карму. "
        "Иногда человек долго живёт «по накатанной», не замечая, что его связи и встречи несут смысл, "
        "что повторяющиеся ситуации — это приглашение увидеть что‑то важное.\n\n"
        "Светолада не даёт готовых ответов и не определяет вашу миссию. "
        "Она лишь подсвечивает направления, которые видны в вашей карте: через качества, которые вы развиваете; "
        "через уроки, которые повторяются; через кармические узлы, которые требуют внимания; "
        "через тень, которую важно увидеть; через желания, которые ведут к росту или уводят в сторону.\n\n"
        "Миссия — это не вывод и не итог. Это не формула, которую можно подвести под черту. "
        "Это направление, которое вы чувствуете внутри, когда появляется тихое понимание: «да, это моё». "
        "Светолада лишь помогает увидеть то, что уже есть. А дальше — путь души вы проходите сами."
    )

    if age <= 11:
        strengths = (
            "У вас светлая, чистая энергия. Вы тянетесь к тому, что вызывает радость, "
            "и быстро раскрываете свои способности, если рядом есть тепло и поддержка."
        )
        weaknesses = (
            "Иногда вы можете уставать или терять интерес, если что‑то не совпадает "
            "с вашим внутренним откликом. Это естественно — вы только учитесь слушать себя."
        )
        recommendations = (
            "Вам подходит игра, творчество, мягкая атмосфера и люди, которые помогают "
            "вам чувствовать себя в безопасности. Через интерес вы раскрываете свой путь."
        )
        summary = (
            "Ваш путь души только начинается. Вы растёте, открываете мир и постепенно "
            "узнаёте, что делает вас счастливыми."
        )

    elif 12 <= age <= 17:
        strengths = (
            "У вас яркая, живая энергия. Вы ищете себя, пробуете новое, "
            "и ваша душа активно раскрывается через опыт, эмоции и самопознание."
        )
        weaknesses = (
            "Иногда вы можете сомневаться в себе, переживать слишком остро "
            "или пытаться соответствовать ожиданиям других."
        )
        recommendations = (
            "Слушайте свои чувства, пробуйте новое, не бойтесь менять направление. "
            "Вы растёте, и это нормально — искать, ошибаться, снова искать и находить."
        )
        summary = (
            "Ваш путь души раскрывается активно. Вы учитесь понимать свои желания, "
            "границы и внутренние ориентиры."
        )

    else:
        strengths = (
            "Вы обладаете зрелой, устойчивой энергией. Ваш путь души связан с глубиной, "
            "честностью, внутренней ясностью и стремлением жить в соответствии с собой."
        )
        weaknesses = (
            "Иногда вы можете чувствовать сомнения или внутреннее напряжение, "
            "если идёте путём, который не откликается в сердце."
        )
        recommendations = (
            "Слушайте себя, выбирайте честность, спокойный ритм и то, что приносит "
            "внутренний смысл. Ваш путь раскрывается мягко, когда вы не торопите себя."
        )
        summary = (
            "Ваш путь души показывает зрелость, глубину и способность идти своим путём "
            "даже тогда, когда он отличается от ожиданий других."
        )

    return {
        "key": key,
        "strengths": strengths,
        "weaknesses": weaknesses,
        "recommendations": recommendations,
        "summary": summary
    }


