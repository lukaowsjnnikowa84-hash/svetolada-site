def get_karma(age: int, gender: str, has_time: bool):
    key = (
        "Карма — это не наказание и не долг. Это набор внутренних уроков, "
        "которые душа выбирает для роста. Это темы, которые повторяются в жизни, "
        "пока не будут прожиты мягко, осознанно и с любовью к себе."
    )

    strengths = (
        "Вы обладаете редкой способностью чувствовать глубину происходящего. "
        "Ваш опыт — даже сложный — превращается в мудрость. "
        "Вы умеете замечать повторяющиеся ситуации, понимать их смысл "
        "и постепенно освобождаться от старых сценариев. "
        "Ваше сердце сильнее, чем вы думаете."
    )

    weaknesses = (
        "Иногда вы можете возвращаться к прошлым переживаниям, мыслям или людям, "
        "которые уже не соответствуют вашему пути. "
        "Это не ошибка — это знак, что внутри есть незавершённое чувство, "
        "которое просит внимания. "
        "Карма проявляется мягко, когда вы позволяете себе чувствовать, "
        "а не подавляете эмоции."
    )

    recommendations = (
        "Вам важно выбирать честность с собой, даже если она приводит к переменам. "
        "Замедляйтесь, когда чувствуете внутреннее напряжение — это момент, "
        "когда карма поднимает старый слой, чтобы вы могли его отпустить. "
        "Поддерживайте себя: тёплыми словами, спокойным ритмом, "
        "людьми, рядом с которыми вы чувствуете безопасность. "
        "Ваш путь — не борьба, а мягкое освобождение."
    )

    summary = (
        "Ваш кармический путь — это путь очищения, внутренней силы и пробуждения. "
        "Вы постепенно освобождаетесь от старых узлов, становитесь легче, чище, "
        "мудрее. Карма не тянет вас назад — она ведёт вперёд, "
        "к более свободной версии себя."
    )

    return {
        "key": key,
        "strengths": strengths,
        "weaknesses": weaknesses,
        "recommendations": recommendations,
        "summary": summary
    }

